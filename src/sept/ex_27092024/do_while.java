package sept.ex_27092024;

public class do_while {
    //  do while:  int -  body -  condition -  incre/decre
//  while: init - condition - body  - incre/decre
    public static void main(String[] args) {
        int a = 0;
        do {
            System.out.println("Print" + a);
            a++;
        } while (a < 10);
    }
}
